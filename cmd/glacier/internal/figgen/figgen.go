// Render converts name into a 5-element []string, each element being one row
// of the rendered banner. Characters outside [a-zA-Z0-9 _-] are stripped
// before rendering. Unknown characters after sanitisation fall back to the
// space glyph.
func Render(name string) []string {
	rows := make([]string, glyphHeight)
	for _, r := range sanitise(name) {
		upper := unicode.ToUpper(r)
		g, ok := glyphs[upper]
		if !ok {
			g = glyphs[' ']
		}
		for i := range glyphHeight {
			rows[i] += g[i]
		}
	}
	return rows
}

// sanitise removes any rune not in [a-zA-Z0-9 _-] from s.
func sanitise(s string) string {
	var b strings.Builder
	b.Grow(len(s))
	for _, r := range s {
		if (r >= 'a' && r <= 'z') ||
			(r >= 'A' && r <= 'Z') ||
			(r >= '0' && r <= '9') ||
			r == ' ' || r == '_' || r == '-' {
			b.WriteRune(r)
		}
	}
	return b.String()
}

package gen

import (
	"fmt"
	"go/format"
	"io"
	"sort"
	"strconv"
	"strings"
)

const (
	generatedHeader = "// Code generated by glaciergen; DO NOT EDIT."
	defaultOutput   = "zz_generated_cli.go"
)

// emitRegistrations generates the full Go source for a zz_generated_cli.go file.
// All string literals pass through strconv.Quote (§23.8 invariant).
func emitRegistrations(w io.Writer, pkgName string, commands []DiscoveredCommand, appName string) error {
	var b strings.Builder

	b.WriteString(generatedHeader)
	b.WriteString("\n\n")
	b.WriteString("package ")
	b.WriteString(pkgName)
	b.WriteString("\n\n")

	// Collect imports needed.
	b.WriteString("import (\n")
	b.WriteString("\t\"github.com/nathanbrophy/glacier/cli\"\n")
	b.WriteString(")\n\n")

	b.WriteString("func init() {\n")

	// Sort commands for deterministic output.
	sorted := make([]DiscoveredCommand, len(commands))
	copy(sorted, commands)
	sort.Slice(sorted, func(i, j int) bool {
		return sorted[i].TypeName < sorted[j].TypeName
	})

	for _, cmd := range sorted {
		pr := cmd.ParseResult
		target := pr.Cmd.App
		if target == "" {
			target = "cli.Default"
		}
		if appName != "" && pr.Cmd.App == "cli.Default" {
			target = appName
		}

		b.WriteString("\tif err := ")
		b.WriteString(target)
		b.WriteString(".Register(&")
		b.WriteString(cmd.TypeName)
		b.WriteString("{}")

		// Collect option calls.
		var opts []string

		if pr.IsRoot {
			opts = append(opts, "cli.WithRoot()")
		}
		if pr.Cmd.Name != "" {
			opts = append(opts, "cli.WithName("+strconv.Quote(pr.Cmd.Name)+")")
		}
		if pr.Cmd.Parent != "" {
			opts = append(opts, "cli.WithParent("+strconv.Quote(pr.Cmd.Parent)+")")
		}
		if pr.Cmd.Alias != "" {
			opts = append(opts, "cli.WithAlias("+strconv.Quote(pr.Cmd.Alias)+")")
		}

		// Field-level markers — sort field names for determinism.
		fieldNames := make([]string, 0, len(pr.Fields))
		for fn := range pr.Fields {
			fieldNames = append(fieldNames, fn)
		}
		sort.Strings(fieldNames)

		for _, fn := range fieldNames {
			fm := pr.Fields[fn]
			qn := strconv.Quote(fn)
			if fm.HasShort {
				opts = append(opts, fmt.Sprintf("cli.WithFlagShort(%s, %s)", qn, quoteRune(fm.Short)))
			}
			if fm.Env != "" {
				opts = append(opts, fmt.Sprintf("cli.WithFlagEnv(%s, %s)", qn, strconv.Quote(fm.Env)))
			}
			if fm.Required {
				opts = append(opts, fmt.Sprintf("cli.WithFlagRequired(%s)", qn))
			}
			if len(fm.Choices) > 0 {
				quotedChoices := make([]string, len(fm.Choices))
				for i, ch := range fm.Choices {
					quotedChoices[i] = strconv.Quote(ch)
				}
				opts = append(opts, fmt.Sprintf("cli.WithFlagChoices(%s, %s)", qn, strings.Join(quotedChoices, ", ")))
			}
			if fm.HasDepr {
				opts = append(opts, fmt.Sprintf("cli.WithFlagDeprecated(%s, %s)", qn, strconv.Quote(fm.Deprecated)))
			}
			if fm.Validate != "" {
				opts = append(opts, fmt.Sprintf("cli.WithFlagValidate(%s, %s)", qn, fm.Validate))
			}
		}

		if len(opts) > 0 {
			b.WriteString(",\n")
			for _, opt := range opts {
				b.WriteString("\t\t")
				b.WriteString(opt)
				b.WriteString(",\n")
			}
			b.WriteString("\t")
		}

		b.WriteString("); err != nil {\n")
		b.WriteString("\t\tpanic(err)\n")
		b.WriteString("\t}\n")
	}

	b.WriteString("}\n")

	// Format the generated source with gofmt.
	src := b.String()
	formatted, err := format.Source([]byte(src))
	if err != nil {
		// Return unformatted on parse error (caller will see bad source).
		return fmt.Errorf("gen: format generated source: %w (source: %s)", err, src)
	}

	_, err = w.Write(formatted)
	return err
}

// quoteRune returns a Go rune literal string like '\'p\''.
func quoteRune(r rune) string {
	return strconv.QuoteRune(r)
}

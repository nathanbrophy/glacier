// emitMocks writes the full Go source for a zz_generated_mocks.go file to w.
// All string literals pass through strconv.Quote (§23.8 invariant).
func emitMocks(w io.Writer, pkgName string, ifaces []DiscoveredInterface) error {
	// Collect the imports each interface contributes by walking every type
	// expression. typesToShortForm doubles as: input full TypeExpr ->
	// canonical short form, with imports captured along the way.
	imports := map[string]struct{}{
		"github.com/nathanbrophy/glacier/mock": {},
	}
	for _, iface := range ifaces {
		for _, m := range iface.Methods {
			for _, p := range m.Params {
				_ = collectImports(p.TypeExpr, pkgName, iface.PkgPath, imports)
			}
			for _, r := range m.Results {
				_ = collectImports(r.TypeExpr, pkgName, iface.PkgPath, imports)
			}
		}
	}

	var b strings.Builder
	b.WriteString(generatedHeader)
	b.WriteString("\n\n")
	b.WriteString("package ")
	b.WriteString(pkgName)
	b.WriteString("\n\n")

	// Emit imports in sorted order.
	if len(imports) > 0 {
		sortedImports := make([]string, 0, len(imports))
		for path := range imports {
			sortedImports = append(sortedImports, path)
		}
		sort.Strings(sortedImports)
		b.WriteString("import (\n")
		for _, path := range sortedImports {
			b.WriteString("\t")
			b.WriteString(strconv.Quote(path))
			b.WriteString("\n")
		}
		b.WriteString(")\n\n")
	}

	// Sort interfaces for deterministic output.
	sorted := make([]DiscoveredInterface, len(ifaces))
	copy(sorted, ifaces)
	sort.Slice(sorted, func(i, j int) bool {
		return sorted[i].TypeName < sorted[j].TypeName
	})

	for _, iface := range sorted {
		if err := emitWrapper(&b, pkgName, iface); err != nil {
			return err
		}
	}

	formatted, err := format.Source([]byte(b.String()))
	if err != nil {
		return fmt.Errorf("mockgen: format generated source: %w (source: %s)", err, b.String())
	}
	_, err = w.Write(formatted)
	return err
}

// emitWrapper writes the Mock wrapper struct and all its On<Method> builders
// for a single interface. pkgName is the package the mock is being emitted
// into; it is used to strip same-package qualifiers from type expressions.
func emitWrapper(b *strings.Builder, pkgName string, iface DiscoveredInterface) error {
	wrapperName := iface.TypeName + "Mock"

	// --- Wrapper struct ---
	fmt.Fprintf(b, "// %s is a generated typed mock for the %s interface.\n", wrapperName, iface.TypeName)
	fmt.Fprintf(b, "// Use On<MethodName>(...) to register expectations.\n")
	fmt.Fprintf(b, "type %s struct {\n", wrapperName)
	fmt.Fprintf(b, "\tmock.Mock[%s]\n", iface.TypeName)
	fmt.Fprintf(b, "}\n\n")

	// --- On<Method> builders ---
	for _, m := range iface.Methods {
		expectTypeName := iface.TypeName + "MockExpectation_" + m.Name
		if err := emitOnMethod(b, pkgName, iface.PkgPath, wrapperName, expectTypeName, iface.TypeName, m); err != nil {
			return err
		}
		if err := emitExpectationType(b, pkgName, iface.PkgPath, expectTypeName, m); err != nil {
			return err
		}
	}

	return nil
}

// emitOnMethod writes the On<MethodName> method on the wrapper.
func emitOnMethod(b *strings.Builder, pkgName, ifacePkgPath, wrapperName, expectTypeName, ifaceName string, m DiscoveredMethod) error {
	paramList := make([]string, len(m.Params))
	for i, p := range m.Params {
		paramList[i] = fmt.Sprintf("m%d mock.Matcher[%s]", i, sanitizeType(p.TypeExpr, pkgName, ifacePkgPath))
	}

	fmt.Fprintf(b, "// On%s starts building an expectation for the %s method.\n", m.Name, strconv.Quote(m.Name))
	fmt.Fprintf(b, "func (w *%s) On%s(%s) *%s {\n",
		wrapperName, m.Name, strings.Join(paramList, ", "), expectTypeName)

	if len(m.Params) > 0 {
		matcherArgs := make([]string, len(m.Params))
		for i := range m.Params {
			matcherArgs[i] = fmt.Sprintf("m%d", i)
		}
		fmt.Fprintf(b, "\te := w.OnCall(%s).With(%s)\n",
			strconv.Quote(m.Name),
			strings.Join(matcherArgs, ", "))
	} else {
		fmt.Fprintf(b, "\te := w.OnCall(%s)\n", strconv.Quote(m.Name))
	}
	fmt.Fprintf(b, "\treturn &%s{Expectation: e}\n", expectTypeName)
	fmt.Fprintf(b, "}\n\n")
	return nil
}

// emitExpectationType writes the typed expectation struct for one method.
func emitExpectationType(b *strings.Builder, pkgName, ifacePkgPath, expectTypeName string, m DiscoveredMethod) error {
	fmt.Fprintf(b, "// %s is the typed expectation builder for the %s method.\n",
		expectTypeName, strconv.Quote(m.Name))
	fmt.Fprintf(b, "type %s struct{ *mock.Expectation }\n\n", expectTypeName)

	retParams := make([]string, len(m.Results))
	retArgs := make([]string, len(m.Results))
	for i, r := range m.Results {
		retParams[i] = fmt.Sprintf("r%d %s", i, sanitizeType(r.TypeExpr, pkgName, ifacePkgPath))
		retArgs[i] = fmt.Sprintf("r%d", i)
	}
	fmt.Fprintf(b, "// Return sets the fixed return values for this expectation.\n")
	fmt.Fprintf(b, "func (e *%s) Return(%s) *%s {\n",
		expectTypeName, strings.Join(retParams, ", "), expectTypeName)
	if len(retArgs) > 0 {
		fmt.Fprintf(b, "\te.Expectation.Return(%s)\n", strings.Join(retArgs, ", "))
	} else {
		fmt.Fprintf(b, "\te.Expectation.Return()\n")
	}
	fmt.Fprintf(b, "\treturn e\n")
	fmt.Fprintf(b, "}\n\n")

	return nil
}

// sanitizeType converts a fully-qualified type expression from
// types.TypeString into a Go source expression suitable for the generated
// file in pkgName. Same-package qualifiers are stripped.
//
// Examples (with pkgName = "ghreleases", ifacePkgPath = ".../ghreleases"):
//
//	"github.com/.../ghreleases.Release" → "Release"
//	"context.Context"                   → "context.Context"
//	"string"                            → "string"
//	"[]github.com/.../ghreleases.Release" → "[]Release"
func sanitizeType(typeExpr, pkgName, ifacePkgPath string) string {
	// Replace the full ifacePkgPath qualifier with empty (strip self-qualifier).
	// We do this BEFORE the slash-stripping pass because the path contains slashes.
	if ifacePkgPath != "" {
		typeExpr = strings.ReplaceAll(typeExpr, ifacePkgPath+".", "")
	}

	// For each remaining slash-separated segment, keep only the last
	// "pkg.Name" component (the imports block carries the full path).
	// Walk the string and replace runs of slashed-import paths with their tail.
	result := strings.Builder{}
	i := 0
	for i < len(typeExpr) {
		// Find the next path-like prefix (anything containing a slash followed
		// by a dot for the type selector). Heuristic: scan for "/" and rewind
		// to the start of the path token.
		idx := strings.IndexByte(typeExpr[i:], '/')
		if idx < 0 {
			result.WriteString(typeExpr[i:])
			break
		}
		absIdx := i + idx
		// Walk backward from absIdx to the start of the import path token
		// (start = previous non-identifier char + 1).
		start := absIdx
		for start > i {
			c := typeExpr[start-1]
			if !isIdentChar(c) && c != '.' && c != '/' {
				break
			}
			start--
		}
		// Walk forward from absIdx to the end of "pkg.Name" form.
		end := absIdx
		for end < len(typeExpr) {
			c := typeExpr[end]
			if !isIdentChar(c) && c != '.' && c != '/' {
				break
			}
			end++
		}
		// typeExpr[start:end] is e.g. "github.com/foo/bar.Baz"; keep only "bar.Baz".
		segment := typeExpr[start:end]
		if lastSlash := strings.LastIndex(segment, "/"); lastSlash >= 0 {
			segment = segment[lastSlash+1:]
		}
		// If segment short form starts with "pkgName.", strip it.
		if strings.HasPrefix(segment, pkgName+".") {
			segment = segment[len(pkgName)+1:]
		}
		// Append everything before the path token verbatim, then the trimmed segment.
		result.WriteString(typeExpr[i:start])
		result.WriteString(segment)
		i = end
	}
	return result.String()
}

// collectImports walks typeExpr and adds every import path it references to
// the imports set. Same-package references (matching ifacePkgPath) are skipped.
// Stdlib selectors are resolved via the stdlibImports map.
func collectImports(typeExpr, pkgName, ifacePkgPath string, imports map[string]struct{}) error {
	// Strip self-package full path first.
	if ifacePkgPath != "" {
		typeExpr = strings.ReplaceAll(typeExpr, ifacePkgPath+".", "")
	}

	// Walk the expression, finding every "pkg.Name" or "path/pkg.Name" form.
	i := 0
	for i < len(typeExpr) {
		// Find the next dot that's a selector (not part of a number etc).
		idx := strings.IndexByte(typeExpr[i:], '.')
		if idx < 0 {
			break
		}
		absIdx := i + idx
		// Walk backward to find the start of the package selector.
		start := absIdx
		for start > 0 {
			c := typeExpr[start-1]
			if !isIdentChar(c) && c != '/' {
				break
			}
			start--
		}
		if start == absIdx {
			// no identifier before the dot; skip.
			i = absIdx + 1
			continue
		}
		// Walk forward past the type selector.
		end := absIdx + 1
		for end < len(typeExpr) {
			c := typeExpr[end]
			if !isIdentChar(c) {
				break
			}
			end++
		}

		segment := typeExpr[start:absIdx] // the "pkg" or "github.com/foo/pkg" part
		if segment == pkgName {
			// Same-package self-qualifier; skip.
			i = end
			continue
		}
		if strings.Contains(segment, "/") {
			imports[segment] = struct{}{}
		} else if path, ok := stdlibImports[segment]; ok {
			imports[path] = struct{}{}
		}
		// else: unknown short package; the user's imports block must cover it
		// already (e.g. third-party non-stdlib types). Leave untouched.

		i = end
	}
	return nil
}

// isIdentChar reports whether c is part of a Go identifier (excluding '.').
func isIdentChar(c byte) bool {
	return (c >= 'a' && c <= 'z') ||
		(c >= 'A' && c <= 'Z') ||
		(c >= '0' && c <= '9') ||
		c == '_'
}

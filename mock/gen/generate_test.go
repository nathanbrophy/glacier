// fakeDiscoverer is a test double for gen.Discoverer. It returns a fixed
// slice without invoking go/packages. It satisfies gen.Discoverer.
type fakeDiscoverer struct {
	result []gen.DiscoveredInterface
	err    error
}

func (f *fakeDiscoverer) Discover(_, _ string, _ *slog.Logger) ([]gen.DiscoveredInterface, error) {
	return f.result, f.err
}

var _ gen.Discoverer = (*fakeDiscoverer)(nil)

// oneMethodIface returns a minimal DiscoveredInterface with a single method.
func oneMethodIface(pkgDir, pkgName, typeName string) gen.DiscoveredInterface {
	return gen.DiscoveredInterface{
		PkgPath:  "github.com/example/testpkg",
		PkgDir:   pkgDir,
		PkgName:  pkgName,
		TypeName: typeName,
		Methods: []gen.DiscoveredMethod{
			{
				Name:    "Fetch",
				Params:  []gen.DiscoveredParam{{TypeExpr: "string"}},
				Results: []gen.DiscoveredResult{{TypeExpr: "string"}, {TypeExpr: "error"}},
			},
		},
	}
}

